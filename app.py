# ---------- MAIN APP ----------
def main_app():
    model = load_model()
    c1, c2 = st.columns([4, 1])
    with c1:
        st.markdown(
            f"<div style='background:{NAVY};padding:18px 26px;border-radius:12px;border-left:10px solid {CYAN};'>"
            f"<h2 style='color:white;margin:0;font-size:30px;'>🦴 Bone Fracture Detection</h2></div>",
            unsafe_allow_html=True)
    with c2:
        st.write("")
        if st.button("🚪 Log out"):
            st.session_state.auth = False
            st.rerun()

    st.markdown(
        f"<p style='color:{NAVY};font-size:19px;font-weight:600;'>"
        f"Welcome, {st.session_state.user} — upload a DICOM or image, then run the detection.</p>",
        unsafe_allow_html=True)
    uploaded = st.file_uploader("Upload an X-ray (DICOM .dcm or JPG / PNG)",
                                type=["dcm", "dicom", "jpg", "jpeg", "png"])

    if uploaded is not None:
        image, ftype = load_any(uploaded)
        if st.button("🔍 Run Prediction", type="primary"):
            results = model.predict(source=np.array(image), verbose=False)[0]
            n = len(results.boxes)
            confs = [round(float(c) * 100) for c in results.boxes.conf] if n else []
            boxes = [list(map(float, b)) for b in results.boxes.xyxy.cpu().numpy()] if n else []
            out_image = Image.fromarray(cv2.cvtColor(results.plot(), cv2.COLOR_BGR2RGB))

            colA, colB = st.columns(2)
            with colA:
                st.subheader("Input Scan"); st.image(image, use_container_width=True)
            with colB:
                st.subheader("AI Detection"); st.image(out_image, use_container_width=True)

            if n > 0:
                st.error(f"⚠️ FRACTURE DETECTED — {n} region(s), confidence: {', '.join(str(c)+'%' for c in confs)}")
            else:
                st.success("✅ No fracture detected.")

            case_id = "SC-" + datetime.datetime.now().strftime("%Y%m%d-%H%M%S")
            pdf = build_pdf(case_id, ftype, n > 0, n, confs, boxes, image, out_image)
            st.download_button("📄 Download PDF Report", data=pdf,
                               file_name=f"{case_id}_fracture_report.pdf", mime="application/pdf")

    st.caption("Educational demo — not a medical device.")


# ---------- ROUTER ----------
if "auth" not in st.session_state:
    st.session_state.auth = False

if st.session_state.auth:
    main_app()
else:
    login_screen()
