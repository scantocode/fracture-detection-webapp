import os, io, datetime
import numpy as np
import cv2
from PIL import Image
import streamlit as st
from ultralytics import YOLO

# ---------- config ----------
MODEL_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "best.pt")
NAVY = "#0A1628"; CYAN = "#4FD1C5"; ORANGE = "#FF6A3D"

# demo login (for a real app, use a database + hashing)
USERS = {"admin": "scan2code", "doctor": "fracture123"}

st.set_page_config(page_title="Scan to Code - Fracture Detection",
                   page_icon="🦴", layout="wide")


@st.cache_resource
def load_model():
    return YOLO(MODEL_PATH)


# ---------- DICOM / image loading ----------
def load_dicom(file_bytes):
    import pydicom
    ds = pydicom.dcmread(io.BytesIO(file_bytes))
    arr = ds.pixel_array.astype(float)
    arr = (arr - arr.min()) / (arr.max() - arr.min() + 1e-8) * 255.0
    arr = arr.astype(np.uint8)
    if arr.ndim == 2:
        arr = cv2.cvtColor(arr, cv2.COLOR_GRAY2RGB)
    return Image.fromarray(arr)


def load_any(uploaded):
    name = uploaded.name.lower()
    data = uploaded.read()
    if name.endswith(".dcm") or name.endswith(".dicom"):
        return load_dicom(data), "DICOM"
    return Image.open(io.BytesIO(data)).convert("RGB"), "Image"


# ---------- PDF report ----------
def build_pdf(case_id, file_type, detected, n, confs, boxes, input_img, output_img):
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.units import mm
    from reportlab.lib import colors
    from reportlab.platypus import (SimpleDocTemplate, Paragraph, Spacer, Table,
                                    TableStyle, Image as RLImage)
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle

    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=A4, topMargin=18 * mm, bottomMargin=15 * mm)
    styles = getSampleStyleSheet()
    navy = colors.HexColor(NAVY); cyan = colors.HexColor(CYAN); orange = colors.HexColor(ORANGE)

    h_title = ParagraphStyle("t", parent=styles["Title"], textColor=navy, fontSize=20)
    h_sub = ParagraphStyle("s", parent=styles["Normal"], textColor=cyan, fontSize=11)
    h_sec = ParagraphStyle("sec", parent=styles["Heading2"], textColor=navy, fontSize=13)
    small = ParagraphStyle("sm", parent=styles["Normal"], fontSize=8, textColor=colors.grey)

    el = []
    el.append(Paragraph("Scan to Code", h_title))
    el.append(Paragraph("Bone Fracture Detection Report", h_sub))
    el.append(Spacer(1, 10))

    # case info
    info = [
        ["Report ID", case_id, "Date", datetime.datetime.now().strftime("%Y-%m-%d %H:%M")],
        ["Input type", file_type, "Model", "YOLO11 Medium"],
    ]
    t = Table(info, colWidths=[70, 150, 60, 150])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (0, -1), navy), ("BACKGROUND", (2, 0), (2, -1), navy),
        ("TEXTCOLOR", (0, 0), (0, -1), colors.white), ("TEXTCOLOR", (2, 0), (2, -1), colors.white),
        ("FONTSIZE", (0, 0), (-1, -1), 9), ("GRID", (0, 0), (-1, -1), 0.5, colors.lightgrey),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"), ("LEFTPADDING", (0, 0), (-1, -1), 6),
        ("TOPPADDING", (0, 0), (-1, -1), 6), ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
    ]))
    el.append(t); el.append(Spacer(1, 14))

    # result
    el.append(Paragraph("Result", h_sec))
    if detected:
        res = ParagraphStyle("r", parent=styles["Normal"], fontSize=16, textColor=orange, leading=20)
        el.append(Paragraph(f"<b>FRACTURE DETECTED</b> &nbsp; ({n} region{'s' if n > 1 else ''})", res))
        if confs:
            el.append(Paragraph(f"Confidence: {', '.join(str(c) + '%' for c in confs)}", styles["Normal"]))
    else:
        res = ParagraphStyle("r", parent=styles["Normal"], fontSize=16, textColor=colors.green, leading=20)
        el.append(Paragraph("<b>NO FRACTURE DETECTED</b>", res))
    if detected:
        el.append(Paragraph(
            "The fracture region(s) are highlighted with bounding boxes on the "
            "detection image below.", styles["Normal"]))
    el.append(Spacer(1, 14))

    # images
    el.append(Paragraph("Input Scan vs. AI Detection", h_sec))
    el.append(Spacer(1, 6))

    def rl(img, w=240):
        b = io.BytesIO(); img.save(b, format="PNG"); b.seek(0)
        ar = img.height / img.width
        return RLImage(b, width=w, height=w * ar)

    img_tbl = Table([[rl(input_img), rl(output_img)],
                     [Paragraph("Input", small), Paragraph("Detection", small)]],
                    colWidths=[250, 250])
    img_tbl.setStyle(TableStyle([("ALIGN", (0, 0), (-1, -1), "CENTER")]))
    el.append(img_tbl)
    el.append(Spacer(1, 18))

    el.append(Paragraph(
        "DISCLAIMER: This report is generated by an educational AI demonstration and is "
        "NOT a medical device. It must not be used for clinical diagnosis or treatment. "
        "Always consult a qualified radiologist.", small))
    el.append(Spacer(1, 4))
    el.append(Paragraph("Generated by Scan to Code  |  youtube.com/@scantocode", small))

    doc.build(el)
    buf.seek(0)
    return buf.getvalue()


